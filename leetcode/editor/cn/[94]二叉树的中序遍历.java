    public List<Integer> inorderTraversal(TreeNode root){
        List<Integer> res = new ArrayList<>();
        Deque<TreeNode> stk = new LinkedList<TreeNode>();

        while(root != null || !stk.isEmpty()){
            while (root != null){
                stk.push(root);
                root = root.left;
            }
            stk.pop();
            res.add(root.val);
            root = root.right;
        }
        return res;
    }



