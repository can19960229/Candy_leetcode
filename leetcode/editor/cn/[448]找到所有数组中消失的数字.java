    public List<Integer> findDisappearedNumbers(int[] nums) {
        ArrayList<Integer> res = new ArrayList<>();
        for (int i = 0; i < nums.length; i++) {
            if (nums[Math.abs(nums[i]) - 1]> 0){
                nums[Math.abs(nums[i]) - 1] *= (-1);
            }
           // System.out.println(nums[i]);
        }
        for (int i = 0; i < nums.length; i++) {
            //System.out.println(nums[i]);
            if (nums[i] > 0){
                res.add(i+1);
            }
        }
        return res;
    }
