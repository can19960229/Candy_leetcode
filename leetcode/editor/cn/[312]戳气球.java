class Solution_312 {
    public int maxCoins(int[] nums) {
        int n = nums.length;
        //æ·»åŠ ä¸¤ä¾§çš„è™šæ‹Ÿæ°”çƒ
        int[] points = new int[n + 2];
        //nums[-1] = nums[n] = 1,ç›´æ¥æŠŠè¿™ä¸¤ä¸ªè¾¹ç•ŒåŠ è¿›å»ï¼Œå½¢æˆä¸€ä¸ªæ–°çš„æ•°ç»„
        points[0] = points[n +1] = 1;
        for (int i = 1; i <= n; i++) {
            points[i] = nums[i - 1];
        }
        //base case
        int[][] dp = new int[n + 2][n + 2];
        //å¼€å§‹çŠ¶æ€è½¬ç§»
        //iåº”è¯¥ä»ä¸‹åˆ°ä¸Š
        for (int i = n; i >= 0; i--) {
            //jåº”è¯¥ä»å·¦åˆ°å³
            for (int j = i + 1; j < n + 2; j++) {
                //æœ€åæˆ³ç ´çš„æ°”çƒæ˜¯å“ªä¸ª
                for(int k = i + 1; k < j; k++){
                    //æ‹©ä¼˜åšé€‰æ‹©
                    dp[i][j] = Math.max(
                            dp[i][j],
                            dp[i][k] + dp[k][j] + points[i] * points[j] * points[k]
                    );
                }
            }
        }
        return dp[0][n + 1];
    }
}
